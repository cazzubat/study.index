# While

# O While é uma estrutura de repetição que permite com que a gente faça um loop, onde enquanto uma determinada condição for verdadeira, um bloco de código continuará sendo executado.
# O For é uma estrutura de repetição que possibilita que a gente percorra uma coleção (iterável), e para cada valor da coleção um bloco de código será executado.
# A função range é uma função que irá criar um determinado intervalo onde você passa o começo, o final e o passo.


