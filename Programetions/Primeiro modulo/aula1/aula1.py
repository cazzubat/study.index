# Declaração de Variáveis

copo = 'água'  #Essa é uma variável do tipo string

altura = 1.82  #Essa é uma variável do tipo float (ponto flutuante), numero real;

idade = 22  #Essa é uma variável do tipo inteiro;

brasileiro = True #Essa é uma variável do tipo booleano;


#Operadores de comparação
print(4 == 4) #True #(Compara se os valores dos dois operandos são iguais)

print(7 < 3) #False #(Verifica se o valor à esquerda é menor que o valor à direita)

print(3 <= 5) #True #(Verifica se o valor à esquerda é menor ou igual ao valor à direita)

print(4 != 5) #True #(Verifica se os valores são diferentes)


#Operações Aritméticas
print(altura + idade) # Soma

print(idade - 42) # Subtração

print(altura * 2)  # Multiplicação

print(31 / 5) # (Divisão comum)

print(3 ** 2) # (Potenciação)

print(25 // 6) # (Divisão inteira)

print(25 % 2) # (Módulo de, ou resto da divisão inteira)